// 이 소스코드 파일은 주석을 연습하는 파일입니다.
public class CommentsExample {
	
	public static void main(String[] args) {
		// main + ctrl + spacebar 
		/*
		# main함수(매소드)는 프로그램 실행 시 자동으로 작동하는 함수이며
		반드시 필요한 함수입니다.
		
		# 자바코드를 해석하여 실행시키는 자바 가상 머신(JVM)은 
		 프로그램 실행 시 자동으로 main함수를 불러서 내부의 코드를
		 실행시킵니다.
		*/
		
//		빠른 주석 처리 (ctrl + /), 주석해제도 가능
		//sysout + ctrl + spacebar : 빠른 출력 함수를 생성
		System.out.println("안녕하세요!");
		System.out.println("반갑습니다");
		System.out.println(); // 단순 줄 개행
		System.out.println("안녕히가세요~");
		// ctrl + F11 : 실행 단축키
		System.out.println("이봐요");

		
		
		
	}

}
